int32_t IMAI_IMU_mtb_models_count = 0;
mtb_ml_model_t* IMAI_IMU_mtb_models[IMAI_IMU_MAX_MTB_MODELS];

int mtb_init(const void* handle, uint8_t* model_bin, unsigned int model_size, uint8_t* arena_buffer, int arena_size, int npu_priority, char model_name[]) {
	mtb_ml_model_t** model_obj = (mtb_ml_model_t**)handle;

	mtb_ml_model_bin_t model = {
		.model_bin = model_bin,
		.model_size = model_size,
		.arena_size = arena_size
	};

	strncpy(model.name, model_name, MTB_ML_MODEL_NAME_LEN - 1);
	model.name[MTB_ML_MODEL_NAME_LEN - 1] = '\0';

	mtb_ml_model_buffer_t buffer = {
		.tensor_arena = arena_buffer,
		.tensor_arena_size = arena_size
	};

	if (mtb_ml_model_init(&model, &buffer, model_obj) != CY_RSLT_SUCCESS)
		return IPWIN_RET_ERROR;

	if (mtb_ml_init(npu_priority) != CY_RSLT_SUCCESS)
		return IPWIN_RET_ERROR;

#ifdef IMAI_IMU_PROFILING
	if (mtb_ml_model_profile_config(*model_obj, MTB_ML_PROFILE_ENABLE_MODEL) != CY_RSLT_SUCCESS)
		return IPWIN_RET_ERROR;

	IMAI_IMU_mtb_models[IMAI_IMU_mtb_models_count++] = *model_obj;
#endif

	return 0;
}

